package base;

public class ImageHash {

	private int length;
	
	private byte[] hashValue;

	public int getLength() {
		return length;
	}

	public void setLength(int length) {
		this.length = length;
	}

	public byte[] getHashValue() {
		return hashValue;
	}

	public void setHashValue(byte[] hashValue) {
		this.hashValue = hashValue;
	}
}
